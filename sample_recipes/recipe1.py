INGREDIENTS = [
  '1 pound ground chicken, preferably dark meat',
  '2/3 cup panko breadcrumbs',
  '1/3 cup basil pesto, store-bought or homemade',
  '1 large egg',
  '3/4 teaspoon kosher salt',
  '1/4 teaspoon freshly ground black pepper'
]

STEPS = [
  '''Preheat the oven to 400°F.
  Arrange a rack in the top third of the oven and preheat. Line a rimmed baking sheet with parchment
  paper.''',
  '''Make the meatball mixture:
  Place the ground chicken, breadcrumbs, pesto, egg, salt, and pepper in a large bowl. Mix lightly with
  a fork until just combined.''',
  '''Form the meatballs:
  Form the meat mixture into 20 (about 1 1/2-inch) meatballs and place them on the prepared baking
  sheet, spacing them out evenly as you go.''',
  '''Bake the meatballs:
  Bake until browned and an instant-read thermometer inserted into the middle reads 165°F, about 15
  minutes. Switch the oven to broil and continue to cook until the tops of the meatballs turn goldenbrown in spots, 3 to 5 minutes more.
  Serve with additional basil pesto or warmed marinara sauce for dipping.
  Leftovers can be refrigerated in an airtight container for up to 3 days.'''
]
